const x = Math.floor(Math.random() * 100 + 1);
const y = Math.floor(Math.random() * 50 + 1);

console.log(`Sum:  ${x} + ${y} = ${x + y}`)
console.log(`Diff: ${x} - ${y} = ${x - y}`);
console.log(`Prod: ${x} * ${y} = ${x * y}`);
console.log(`Quot: ${x} / ${y} = ${x / y}`);
console.log(`Rem:  ${x} % ${y} = ${x % y}`);